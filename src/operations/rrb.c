#include "push_swap.h"

void	op_rrb(t_stack **b)
{
	t_stack *last;
	t_stack *second_last;

	if (!b || !*b || !(*b)->next)
		return ;
	last = *b;
	while (last->next)
	{
		second_last = last;
		last = last->next;
	}
	second_last->next = NULL;
	last->next = *b;
	*b = last;
	ft_putendl_fd("rrb", 1);
}